import java.util.*;
class Q9{
public static void main(String[]args){
        //Create Scanner Object
        Scanner sc=new Scanner(System.in);
        //Get input for the number
        int n=sc.nextInt();
        //countdown using for loop
	for(int i=n;i>=1;i--){
	System.out.println(n);
	n--;
        }
}
}
